package com.raon.androidworldcup.ItemList;

public class Item {

    private int icon;
    private String text;

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public void setText(String text){
        this.text = text;
    }

    public int getIcon(){
        return this.icon;
    }

    public String getText(){
        return this.text;
    }
}
